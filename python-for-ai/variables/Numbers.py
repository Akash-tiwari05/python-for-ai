#int type
age = 25
score = -10

print(type(age),age)
print(type(score),score)

#floats type
price = 19.99
temperature = -5.5
pi = 3.14159

print(type(price),price)
print(type(temperature),temperature)
print(type(pi),pi)

"""
Why two types?
Python separates integers and floats for efficiency - 
computers can work with whole numbers faster and more precisely
"""

#Basic math operations

# Addition and subtraction
total = 10 + 5     # 15
print(total)
change = 20 - 7    # 13
print(change)

# Multiplication and division
area = 6 * 4
print(area)       # 24
half = 10 / 2      # 5.0 (always returns float)
print(half)

# Powers
squared = 5 ** 2   # 25
print(squared)

cubed = 2 ** 3     # 8
print(cubed)