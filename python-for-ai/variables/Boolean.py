#Boolean
print(type(True))   # <class 'bool'>

#BOOLEAN IN EXPRESSIONS
#Comparison Operators
print("Comparison Operators")
print(18 > 5)    #True
print(18 < 5)    #False
print(18 == 18)
print(18 != 5)
print()

#Logical Operators
print("Logical Operators")
print(True and False)   # False
print(True or False)    # True
print(not True)   # False
print()

#TRUTHY & FALSY VALUES (VERY IMPORTANT 🔥)
print("FALSY VALUES")
print(False)
print(None)
print(0)
print(0.0)
print("")
print([])
print(set())
print({})

print("TRUTHY VALUES")
bool(10)        # True
bool("Akash")   # True
bool([1,2])     # True